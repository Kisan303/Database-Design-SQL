CREATE TABLE rp_inventory (
    WAREHOUSE_ID INT,
    PRODUCT_CODE CHAR(4),
    QOH INT
);

CREATE TABLE rp_warehouse (
    WAREHOUSE_ID INT,
    WHSE_CITY VARCHAR(255)
);

CREATE TABLE rp_customers (
    CUSTOMER_ID INT,
    CUSTOMER_NAME VARCHAR(255),
    STREET VARCHAR(255),
    CUST_CITY VARCHAR(255),
    CUST_STATE CHAR(2),
    ZIP CHAR(6),
    BALANCE DECIMAL(10, 2),
    CREDIT_LIMIT DECIMAL(10, 2),
    REP_ID INT
);

CREATE TABLE rp_reps (
    REP_ID INT,
    LAST_NAME VARCHAR(255),
    FIRST_NAME VARCHAR(255),
    STREET VARCHAR(255),
    REP_CITY VARCHAR(255),
    REP_STATE CHAR(2),
    ZIP CHAR(5),
    COMMISSION DECIMAL(10, 2),
    RATE DECIMAL(3, 2)
);

CREATE TABLE rp_order_lines (
    ORDER_ID INT,
    PRODUCT_CODE CHAR(4),
    QTY_ORDERED INT,
    PRICE_PAID DECIMAL(10, 2)
);

CREATE TABLE rp_orders (
    ORDER_ID INT,
    ORDER_DATE DATE,
    CUSTOMER_ID INT
);

CREATE TABLE rp_products (
    PRODUCT_CODE CHAR(4),
    DESCRIPTION VARCHAR(255),
    CATEGORY CHAR(2),
    SUGGESTED_PRICE DECIMAL(10, 2)
);


